-- EmoEat Oracle Schema

-- Sequences for auto-increment
CREATE SEQUENCE SEQ_USERS START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_ADMIN START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_CLIENT START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_EMOTIONS START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_FOODS START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_EMOTION_FOOD START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_USER_EMOTIONS START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_USER_PROFILE START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_RECOMMENDATIONS START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE SEQ_ACTIVITY_LOG START WITH 1 INCREMENT BY 1;

-- Table USERS
CREATE TABLE USERS (
    ID_USER NUMBER PRIMARY KEY,
    NAME VARCHAR2(100),
    EMAIL VARCHAR2(100) UNIQUE,
    PASSWORD VARCHAR2(255),
    ROLE VARCHAR2(20),
    CREATED_AT TIMESTAMP DEFAULT SYSDATE
);

CREATE OR REPLACE TRIGGER TRG_USERS
BEFORE INSERT ON USERS FOR EACH ROW
BEGIN
    IF :NEW.ID_USER IS NULL THEN
        :NEW.ID_USER := SEQ_USERS.NEXTVAL;
    END IF;
END;
/

-- Table ADMIN
CREATE TABLE ADMIN (
    ID_ADMIN NUMBER PRIMARY KEY,
    ID_USER NUMBER UNIQUE,
    CONSTRAINT FK_ADMIN_USER FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER)
