-- EmoEat MySQL Schema

CREATE DATABASE IF NOT EXISTS emoeat;
USE emoeat;

-- Table USERS
CREATE TABLE USERS (
    ID_USER INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(100),
    EMAIL VARCHAR(100) UNIQUE,
    PASSWORD VARCHAR(255),
    ROLE VARCHAR(20),
    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB;

-- Table ADMIN
CREATE TABLE ADMIN (
    ID_ADMIN INT AUTO_INCREMENT PRIMARY KEY,
    ID_USER INT UNIQUE,
    CONSTRAINT FK_ADMIN_USER FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER)
) ENGINE=InnoDB;

-- Table CLIENT
CREATE TABLE CLIENT (
    ID_CLIENT INT AUTO_INCREMENT PRIMARY KEY,
    ID_USER INT UNIQUE,
    CONSTRAINT FK_CLIENT_USER FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER)
) ENGINE=InnoDB;

-- Table EMOTIONS
CREATE TABLE EMOTIONS (
    ID_EMOTION INT AUTO_INCREMENT PRIMARY KEY,
    EMOTION_NAME VARCHAR(100),
    DESCRIPTION VARCHAR(255)
) ENGINE=InnoDB;

-- Table FOODS
CREATE TABLE FOODS (
    ID_FOOD INT AUTO_INCREMENT PRIMARY KEY,
    FOOD_NAME VARCHAR(100),
    CATEGORY VARCHAR(50),
    CALORIES INT,
    PROTEIN INT,
    CARBS INT,
    FAT INT,
    DESCRIPTION VARCHAR(255)
) ENGINE=InnoDB;

-- Table EMOTION_FOOD
CREATE TABLE EMOTION_FOOD (
    ID_RULE INT AUTO_INCREMENT PRIMARY KEY,
    ID_EMOTION INT,
    ID_FOOD INT,
    INTENSITY INT,
    CONSTRAINT FK_EF_EMOTION FOREIGN KEY (ID_EMOTION) REFERENCES EMOTIONS(ID_EMOTION),
    CONSTRAINT FK_EF_FOOD FOREIGN KEY (ID_FOOD) REFERENCES FOODS(ID_FOOD)
) ENGINE=InnoDB;

-- Table USER_EMOTIONS
CREATE TABLE USER_EMOTIONS (
    ID_USER_EMOTION INT AUTO_INCREMENT PRIMARY KEY,
    ID_USER INT,
    ID_EMOTION INT,
    EMOTION_DATE DATETIME DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_UE_USER FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER),
    CONSTRAINT FK_UE_EMOTION FOREIGN KEY (ID_EMOTION) REFERENCES EMOTIONS(ID_EMOTION)
) ENGINE=InnoDB;

-- Table USER_PROFILE
CREATE TABLE USER_PROFILE (
    ID_PROFILE INT AUTO_INCREMENT PRIMARY KEY,
    ID_USER INT UNIQUE,
    WEIGHT DECIMAL(5,2),
    HEIGHT DECIMAL(5,2),
    ALLERGIES VARCHAR(255),
    GOAL VARCHAR(100),
    CONSTRAINT FK_PROFILE_USER FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER)
) ENGINE=InnoDB;

-- Table RECOMMENDATIONS
CREATE TABLE RECOMMENDATIONS (
    ID_REC INT AUTO_INCREMENT PRIMARY KEY,
    ID_EMOTION INT,
    ID_FOOD INT,
    BENEFIT VARCHAR(255),
    ID_USER INT,
    RECOMMENDATION_DATE DATETIME DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_REC_EMOTION FOREIGN KEY (ID_EMOTION) REFERENCES EMOTIONS(ID_EMOTION),
    CONSTRAINT FK_REC_FOOD FOREIGN KEY (ID_FOOD) REFERENCES FOODS(ID_FOOD),
    CONSTRAINT FK_REC_USER FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER)
) ENGINE=InnoDB;

-- Table ACTIVITY_LOG
CREATE TABLE ACTIVITY_LOG (
    ID_LOG INT AUTO_INCREMENT PRIMARY KEY,
    ID_USER INT,
    ACTION VARCHAR(255),
    LOG_DATE DATETIME DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_LOG_USER FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER)
) ENGINE=InnoDB;

